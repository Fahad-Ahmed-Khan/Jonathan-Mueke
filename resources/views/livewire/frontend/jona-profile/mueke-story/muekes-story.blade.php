                        <p style="font-size: large !important;">
                            From a wiry erratic teenager to a formidable, relentless leader, Hon. Jonathan Mueke, 
											has always been a prodigy in a hurry. Buoyed by a charming self-esteem and disarming 
											chutzpah, the gubernatorial candidate for Kitui County may be soft spoken, but he has
											weathered the hard waters that rocks a politician’s life.
                        </p>
                    </div>
                    <div class="pagination__item">
                        <p style="font-size: large !important;">
                             Not by any small measure: He’s served in government, and felt the trappings of power:
										   “I learnt that when you’re in government, you can make decisions that can affect a population. 
										   And if you have the political will, you can use that power to make those decisions to change people’s
										   lives. So I took interest in leadership.” 
                        </p>
                    </div>
                    <div class="pagination__item">
                        <p style="font-size: large !important;">
                             While remaining noticeably untouched by the tabloid drama, or whiff of overexposure, that can engulf even
											seasoned politicians, Hon. Mueke’s leadership potential has been earmarked in both professional and political
											spheres, earning him honors locally and abroad, including being an elected member of Strathmore ‘Who’s Who in leadership (USA).’ 
											He was also the founding President of the Kenya Community Abroad, Michigan Chapter and is an alumnus of the exclusive Sunwords 
											leadership unusual FFWD program. A hunger to be the best, to whose leadership is defined by servitude, a man who has abstained
											from the fame of politics to master, more than anyone, the politics of fame.
                        </p>
                                <p class="text-center fw-bold" style="font-size: large !important;">Page 1</p>
                    </div>
                    <div class="pagination__item">
                        <p style="font-size: large !important;">
                            I bought my first house when I was 22. I started working at 19 when I got into college. I first ran for office when I was 30. 
											I became a deputy governor at 36.” Ambition, then, has always been a cooly lit fire beneath his sails.
                        </p>
                    </div>
                    <div class="pagination__item">
                        <p style="font-size: large !important;">
                             From a wiry erratic teenager to a formidable, relentless leader, Hon. Jonathan Mueke, 
											has always been a prodigy in a hurry. Buoyed by a charming self-esteem and disarming 
											chutzpah, the gubernatorial candidate for Kitui County may be soft spoken, but he has
											weathered the hard waters that rocks a politician’s life.
                        </p>
                    </div>
                    <div class="pagination__item">
                        <p style="font-size: large !important;">
                             But isn’t punching above your weight the marksman of a true leader? Willing to sacrifice himself for the greater good? 
											Generally cool-blooded, affable, and devoted to unglamorous fundamentals, Hon. Mueke was born into a family of six under
											the stewardship of his parents Dr & Mrs Mueke from Kitui County. He was a prefect all through primary and secondary school. 
											At the university, he was the organising secretary of the African Students Association in the US. He did a Bachelor’s degree 
											in Computer Science (Wayne State University), an MBA in Information Technology (Oakland University).  While still in the USA, 
											he worked at Wayne State University as a Systems Administrator, K-Mart Corporation as a Y2K Systems Developer, DaimlerChrysler 
											Corporation as a Project Manager and FiServ Incorporated as a Technology Services Manager. When it comes to weight, he knows how 
											to pull his own. 
                        </p>
                                <p class="text-center fw-bold" style="font-size: large !important;">Page 2</p>
                    </div>
                    <div class="pagination__item">
                        <p style="font-size: large !important;">
                            His is a career that glides: moving languidly, effortlessly, covering vast tracts of ground without ever really appearing to sprint.
										   He does not wear his work-rate, his physicality, on his sleeve, despite serving as Nairobi’s Deputy Governor, overseeing the Trade & Corporative, 
										   Roads & Infrastructure, Agriculture, Youth and ICT ministries in the County. His appetite for the long nights and tireless days, a burning ambition
										   in leading the County Delivery Unit ensuring that all county projects were executed properly, were sustainable and had the desired impact to the public. 
                        </p>
                    </div>
                    <div class="pagination__item">
                        <p style="font-size: large !important;">
                              “I learnt that the smartest and most experienced people in this country work for the government. It’s something I had not fathomed. I always thought that
										   coming from the private sector you’re more efficient, more experienced, more learned, you can move things much quicker and make good decisions and so you’re 
										   better off than people in government. It is not true. The last thing I learnt is that with goodwill you can do a lot when you are in power.”
                        </p>
                    </div>
                    <div class="pagination__item">
                        <p style="font-size: large !important;">
                             He is, quite nobly, elegant and graceful; a leader of great moments, who sparks into life, then constantly burns. For one thing, he is dedicated, professionally so.
										   For another, he leaves nothing to chance. If he can't win you over with his work rate, his charm, and his down-to-earth modus operandi, he will win you over with preparation.
										   It's not that he's indulged, like a politician; it's that he's competitive, like an athlete.
                        </p>
                                <p class="text-center fw-bold" style="font-size: large !important;">Page 3</p>
                    </div>
					  <div class="pagination__item">
                        <p style="font-size: large !important;">
                            But he also knows what failure is, understands it even. He ran for the Westlands parliamentary seat unsuccessfully in 2007. “I raised money from well-wishers and friends.
										   I had researched the problems of the people of Westlands and came up with a manifesto addressing all of them. I put together a think tank. I did competitive analysis of my competitors.
										   I campaigned hard. When I went to Kihumbuini Primary School in Kangemi I found my campaign team wearing orange T-shirts voting for Fred Gumo. And that’s when I knew I had lost the elections.”
                        </p>
                    </div>
					  <div class="pagination__item">
                        <p style="font-size: large !important;">
                            <strong>11.</strong> It was a baptism by fire. A fire that is now blazing, seeking to provide warmth for others. Hon. Mueke’s fire is in Kitui. Here, he brings his experience, credibility, integrity, competence
										   and transformative leadership skills — just what Kitui needs to face the challenges of lack of water, youth unemployment, infrastructure development, service delivery, inefficiency and change management. 
										   His fire is one that burns with a global network of investors with the capacity to exploit Kitui’s natural & mineral resources to the benefit and wealth creation of the people of Kitui. It’s in his DNA, 
										   his strand of belief, the hair strand distinction between good work and doing work that is good. “I’m auguring toward a substantive public office where I can make decisions that solve people’s problems.”
                        </p>
                    </div>
					 <div class="pagination__item">
                        <p style="font-size: large !important;">
                            <strong>12.</strong> He has termed it ‘Our Challenge’. Because that’s how he tackles things: he takes it up as a challenge. To make it better. To inspire. To lead, teach and serve. He’s already heaved two companies from the ground. 
										“I like challenges. I’m a builder, so I like to build things from scratch. I’ve built two companies from scratch; one which I sold two years ago to a French company. I grew it to be the second largest records and 
										documents management company in the region and then sold it off. I restored a 30-year-old Mercedes 126, 1990 model just the other day and it’s up and running. I recently bought a car that is 46 years old that I'm 
										restoring now—a convertible Mercedes SL 280, 1974. I also spend a lot of my time trying to solve problems through technology. I’m a techie.”
                        </p>
                                <p class="text-center fw-bold" style="font-size: large !important;">Page 4</p>
                    </div>
					 <div class="pagination__item">
                        <p style="font-size: large !important;">
                            He is sweat and bone, blood and pain. That’s the language of the worker. The language of Kitui people. The never-say-die attitude. And like most people, who build the world with their sweat and strain, he does not
										take it for granted. When you know what loss is, you appreciate the gains. But can he restore Kitui to a new found glory? 
                        </p>
                    </div>
					 <div class="pagination__item">
                        <p style="font-size: large !important;">
                             “I think through my leadership journey I’ve learnt to become more calm. In my younger days, I was a little bit aggressive. I wanted things done right and sometimes I would come off as aloof or arrogant. I’ve also become more
										sensitive to other people’s needs than to myself.” In his relentless quest for personal improvement, his work ethic, servitude and leadership is a masterclass in a most important life-lesson: There can always be someone more 
										gifted around the corner, but if you work your backside off, success and fame have no choice but to knock on your door.
                        </p>
                    </div>
					 <div class="pagination__item">
                        <p style="font-size: large !important;">
                           “Now I am looking at how to impact the community, the region and the world.” Hon. Jonathan Mueke. Father of two. Father-figure to many. He rises, head and shoulders, above what he terms as a dire need for change, but those shoulders
										are broad enough to allow us to climb up there with him and see that we’re in this together.
                        </p>
